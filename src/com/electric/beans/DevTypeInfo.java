package com.electric.beans;

/**
 * Created by cherry on 2017/12/16.
 * 联系  设备类型表
 * 类型序号/类型名称/是否使用/设备名称自定义
 */
public class DevTypeInfo {
    private int type_id;
    private String type_name;
    private boolean isused;
    private String dev_selfName;

    public int getType_id() {
        return type_id;
    }

    public void setType_id(int type_id) {
        this.type_id = type_id;
    }

    public String getType_name() {
        return type_name;
    }

    public void setType_name(String type_name) {
        this.type_name = type_name;
    }

    public boolean isIsused() {
        return isused;
    }

    public void setIsused(boolean isused) {
        this.isused = isused;
    }

    public String getDev_selfName() {
        return dev_selfName;
    }

    public void setDev_selfName(String dev_selfName) {
        this.dev_selfName = dev_selfName;
    }
}
