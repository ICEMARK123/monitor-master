package com.electric.beans;

import java.util.Date;

/**
 * Created by cherry on 2017/12/16.
 */
public class MesureSiteInfo {
    private String lineWay;
    private String location;
    private String devDescript;
    private String devCode;
    private int typeId;
    private String machineNo;  //主键
    private String phonenumber;
    private Date saveTime;
    private boolean onlineSituation;
    private Date lastCommTime;
    private DevTypeInfo devTypeInfo;

    public String getLineWay() {
        return lineWay;
    }

    public void setLineWay(String lineWay) {
        this.lineWay = lineWay;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getDevDescript() {
        return devDescript;
    }

    public void setDevDescript(String devDescript) {
        this.devDescript = devDescript;
    }

    public String getDevCode() {
        return devCode;
    }

    public void setDevCode(String devCode) {
        this.devCode = devCode;
    }

    public String getMachineNo() {
        return machineNo;
    }

    public void setMachineNo(String machineNo) {
        this.machineNo = machineNo;
    }

    public int getTypeId() {
        return typeId;
    }

    public void setTypeId(int typeId) {
        this.typeId = typeId;
    }

    public String getPhonenumber() {
        return phonenumber;
    }

    public void setPhonenumber(String phonenumber) {
        this.phonenumber = phonenumber;
    }

    public Date getSaveTime() {
        return saveTime;
    }

    public void setSaveTime(Date saveTime) {
        this.saveTime = saveTime;
    }

    public boolean isOnlineSituation() {
        return onlineSituation;
    }

    public void setOnlineSituation(boolean onlineSituation) {
        this.onlineSituation = onlineSituation;
    }

    public Date getLastCommTime() {
        return lastCommTime;
    }

    public void setLastCommTime(Date lastCommTime) {
        this.lastCommTime = lastCommTime;
    }

    public DevTypeInfo getDevTypeInfo() {
        return devTypeInfo;
    }

    public void setDevTypeInfo(DevTypeInfo devTypeInfo) {
        this.devTypeInfo = devTypeInfo;
    }
}
